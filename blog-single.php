                              <h6>The chocolate making process is more complicated than you’d imagine…</h6>
                              <p>As a chocolate lover, you already know that chocolate comes from the cocoa bean. Perhaps you’ve read our guide to cocoa plants and you know all about the ideal climate, different cocoa varieties and the fascinating non-edible uses of cocoa beans. But do you really know how that reddish-yellow fruit dangling from a tree in some tropical country turns into the delicious bar of chocolate you’ve got stashed in your cupboard?</p>
                              <h6>1. Harvesting</h6>
                              <p>Cocoa plants don’t just bear fruit once a year; instead they can produce fruit intermittently. The main harvesting periods coincide with the rainy seasons of that country, so Colombian beans are often harvested in November and April, whereas the Ghanain cocoa bean harvest will depend on where you are in the country.</p>
                              <h6>2. Fermentation</h6>
                              <p>The second stage involves putting these beans – still covered in their sticky fruit – in piles and covering them with banana leaves. The warm tropical temperatures cause the pulp to break down yeast in the air and turn into alcohol. Then, the beans are mixed gently to expose the beans to oxygen, which then breaks the alcohol down into acetic and lactic acid.</p>
                              <p>The liquid drains away and you are left with a pile of cocoa beans that have plumped up due to the moisture in the environment, and whose bitter flavours have been tempered by the acid. This can take up to eight days and is the first stage of developing the well-known chocolate flavours from the naturally bitter, hard cocoa bean.</p>
                              <h6>3. Drying</h6>
                              <p>To dry the beans, our cocoa farmers spread them out in a single layer and dry them naturally in the sun – depending on the weather, this takes around seven days. This makes sure that there is no more moisture in the beans, which is important as after this stage they are transported to the UK, where we finish the chocolate-making process. We always transport our beans by sea freight, rather than air, because it has a smaller carbon footprint. Our Engaged Ethics programme means we work to be as environmentally sustainable as possible, in every aspect of our business.</p>
                              <h6>4. Cleaning</h6>
                              <p>Once the beans arrive with us at our factory, we run them through a cleaning machine, which gets rid of any remaining dried cocoa pulp or pod that might be attached. This ensures that the flavour isn’t impaired, as small impurities could burn during the roasting process.</p>
                              <h6>5. Roasting</h6>
                              <p>Roasting is one of the most important stages to help bring out the flavour in the cocoa beans. To develop the characteristic cocoa aroma, we rely on our in-depth knowledge of the cocoa beans we use; each type of bean needs a different temperature and a different duration of roasting. We roll the beans over constantly during roasting so that they develop a rich colour and start giving off a delectable chocolatey smell!</p>
                              <h6>6. Shell removal</h6>
                              <p>Once the roasting process has finished, it’s time to remove the bean’s thin outer shell. Roasting makes them brittle, so when we pass the beans through a winnowing machine, they easily crack open.</p>
                              <p>The outer shell is lighter than the beans, so we can use fans to blow them away and separate them from the beans themselves. What’s left are the cocoa nibs, which we use to make our chocolate. However, they can be enjoyed in their own natural right: our cocoa nibs can be enjoyed sprinkled on top of foods for a fruity crunch, mixed into homemade granola or stirred into chilli con carne for a deep cocoa taste.</p>
                              <h6>7. Grinding</h6>
                              <p>We process our cocoa nibs by grinding them continuously between two discs until they break down into small pieces and form a paste. Because of the naturally occurring fats in the product, it forms a paste rather than a powder in the same way that grinding peanuts turns into peanut butter rather than dry peanut flour.</p>
                              <p>Cocoa nibs are just over 53% cocoa butter, and the friction of grinding heats the mixture up, causing the cocoa butter to melt and turn the mixture into a paste, called chocolate liquor. This chocolate liquor can be poured into moulds at this stage and be sold as unsweetened, bitter chocolate – but we think it tastes a lot better after a couple more stages!</p>
                              <h6>8. Separating the cocoa butter from the cocoa.</h6>
                              <p>Cocoa butter is an important part of the chocolate-making process, but we separate it from the cocoa so we can have more control over how much we add to each chocolate bar, as well as using it for our white chocolate.</p>
                              <p>Using a hydraulic press, we subject the chocolate liquor to large amounts of pressure, which squeezes out the cocoa butter. At this stage it is still melted, so it drains away as a yellow liquid which is then collected and filtered for later use.</p>
                              <p>Cocoa butter is what makes chocolate melt; solid at room temperature but with a melting point between 34 and 37℃, it means chocolate melts perfectly on the tongue. When tempered correctly, the cocoa butter also gives the chocolate its lustrous shine.</p>
                              <p>After pressing, we are left with compacted cocoa beans. We can go two ways with this; either we start making chocolate by adding ingredients directly to the compacted chocolate liquor, or we can grind it down further to make cocoa powder. The grinding is important as we need to get the cocoa into miniscule pieces so that they make sumptuously smooth hot chocolate!</p>
                              <h6>9. Adding to the chocolate liquor</h6>
                              <p>his is where we’re going to get a bit vague – we don’t want to give away our secrets! The recipe varies for each chocolate that we make.
                              For milk chocolate, we add milk powder, sugar and cocoa butter. We use various amounts depending on which chocolate we make, from our classic 40% milk, our not-too-sweet 50%, or our revolutionary cocoa-rich but still creamy 65% Supermilk chocolate.
                              For dark chocolate, we skip the milk and up the cocoa butter content a little, depending on the percentage of cocoa we’re using. For a strong 90% we add very little sugar, relying on the natural mellow flavours in our chosen cocoa beans and the cocoa butter to give it a well-rounded taste and a smooth melt. For a 70% chocolate, we use slightly more sugar for a rich but decadent treat.</p>
                              <h6>10. Conching</h6>
                              <p>Conching is a word directly associated with chocolate-making, but what does it actually mean? It’s the process used to turn the tasty-but-grainy mixture into delectably smooth melted chocolate. Conching is important because it affects the flavour in different ways.
                              First of all, we pour the mixture into our conching machines. These basically consist of heavy metal rollers that roll over and over through the chocolate mixture, and this process can take anywhere from a couple of hours to a few days depending on the type of chocolate we’re making. The longer it is conched for, the smoother the chocolate will be!
                              Depending on the speed and the temperature of the conching, the process aerates the chocolate; by exposing the mixture to more oxygen it forces the beans to expel volatile acids. This can help reduce any remaining bitterness in the chocolate.</p>
                              <h6>11. Tempering the chocolate</h6>
                              <p>Now we’ve got our melted chocolate! But, if we just poured it into moulds or used it in our chocolates now, it would be a disappointment. The chocolate would be matte and crumbly, and wouldn’t melt as smoothly as you’re used to from our products.
                              Tempering chocolate is what gives it its satisfying snap and shine. We do it by slowly lowering and raising the temperature of the warm chocolate mix. This makes sure the cocoa butter crystals are exactly the right shape for a firm, shiny chocolate. Once it has been cooled to a stable temperature and is perfectly tempered, we start pouring our chocolate into moulds and start using it to create our chocolates.</p>
